public class Main {
    public static void main(String[] args) {
        // Cada println escribe una línea distinta en la consola.
        System.out.println("Hello, World");

        // El texto entre comillas puede cambiar sin modificar la estructura Java.
        System.out.println("Bienvenido al curso de Java");

        // Esta línea confirma que el programa se ejecuta desde el editor online.
        System.out.println("Este programa se ejecuta desde Replit");
    }
}
